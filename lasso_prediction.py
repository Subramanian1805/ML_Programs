import numpy as np
import pandas as pd
import matplotlib.pyplot as plt

df = pd.read_csv('/content/Sample - Superstore.csv', encoding='latin1')

df.isnull().sum()

df

plt.xlabel('sales')
plt.ylabel('profit')
plt.scatter(df.Sales,df.Profit,color='red',marker='+')

from sklearn import linear_model

df

from sklearn.model_selection import train_test_split
from sklearn.metrics import r2_score

new_df = df.drop('Profit', axis='columns')
profit = df['Profit']

numeric_cols = new_df.select_dtypes(include=['number']).columns

x = new_df[numeric_cols]
y = profit

x_train,x_test,y_train,y_test = train_test_split(x,y,test_size=0.2,random_state=42)

reg = linear_model.LinearRegression()
reg.fit(x_train,y_train)

y_pred = reg.predict(x_test)

score = r2_score(y_test,y_pred)
score

from sklearn.linear_model import Lasso
from sklearn.metrics import mean_squared_error

lasso = Lasso(alpha=0.1)
lasso.fit(x_train,y_train)

train_pred = lasso.predict(x_train)
test_pred = lasso.predict(x_test)

train_r2 = r2_score(y_train,train_pred)
test_r2 = r2_score(y_test,test_pred)

train_mse = mean_squared_error(y_train, train_pred)
test_mse = mean_squared_error(y_test, test_pred)

print("Training Accuracy (R2):", train_r2)

print("Testing Accuracy (R2):", test_r2)

print("Training mse:", train_mse)
print("Testing mse:", test_mse)

from sklearn.model_selection import GridSearchCV
from sklearn.linear_model import Lasso

param_grid = {'alpha': [0.001, 0.01, 0.1, 1, 10, 100]}

lasso_grid = Lasso()

grid_search = GridSearchCV(lasso_grid, param_grid, cv=5, scoring='r2', n_jobs=-1)

grid_search.fit(x_train, y_train)

print(f"Best alpha: {grid_search.best_params_['alpha']}")

best_lasso = grid_search.best_estimator_

from sklearn.metrics import r2_score, mean_squared_error

train_pred_tuned = best_lasso.predict(x_train)
test_pred_tuned = best_lasso.predict(x_test)

train_r2_tuned = r2_score(y_train, train_pred_tuned)
test_r2_tuned = r2_score(y_test, test_pred_tuned)

train_mse_tuned = mean_squared_error(y_train, train_pred_tuned)
test_mse_tuned = mean_squared_error(y_test, test_pred_tuned)

print("--- Tuned Lasso Model Performance ---")
print(f"Training Accuracy (R2): {train_r2_tuned}")
print(f"Testing Accuracy (R2): {test_r2_tuned}")
print(f"Training MSE: {train_mse_tuned}")
print(f"Testing MSE: {test_mse_tuned}")

predicted_profit_tuned = best_lasso.predict([[1, 1000, 0, 1, 0]])[0]
print(f"\nPredicted profit with tuned Lasso model: {predicted_profit_tuned}")

columns_to_drop = ['Profit', 'Order ID', 'Product ID', 'Order Date', 'Ship Date', 'Customer ID', 'Customer Name', 'Row ID']

df_features = df.drop(columns=columns_to_drop)

categorical_cols_for_ohe = df_features.select_dtypes(include=['object']).columns.tolist()

X_all = pd.get_dummies(df_features, columns=categorical_cols_for_ohe, drop_first=True)
y_all = df['Profit']

X_train_all, X_test_all, y_train_all, y_test_all = train_test_split(X_all, y_all, test_size=0.2, random_state=42)

print(f"Shape of X_train_all: {X_train_all.shape}")
print(f"Shape of X_test_all: {X_test_all.shape}")

lasso_full = Lasso()

param_grid_full = {'alpha': [0.001, 0.01, 0.1, 1, 10, 100]}

grid_search_full = GridSearchCV(lasso_full, param_grid_full, cv=5, scoring='r2', n_jobs=-1)

grid_search_full.fit(X_train_all, y_train_all)

print(f"Best alpha for full model: {grid_search_full.best_params_['alpha']}")

best_lasso_full = grid_search_full.best_estimator_

train_pred_full = best_lasso_full.predict(X_train_all)
test_pred_full = best_lasso_full.predict(X_test_all)

train_r2_full = r2_score(y_train_all, train_pred_full)
test_r2_full = r2_score(y_test_all, test_pred_full)

train_mse_full = mean_squared_error(y_train_all, train_pred_full)
test_mse_full = mean_squared_error(y_test_all, test_pred_full)

print("\n--- Tuned Lasso Model Performance (Full Features) ---")
print(f"Training Accuracy (R2): {train_r2_full}")
print(f"Testing Accuracy (R2): {test_r2_full}")
print(f"Training MSE: {train_mse_full}")
print(f"Testing MSE: {test_mse_full}")

